// This class represents each applicant who applies for a listing.
class Applicant {
  String name;
  String dateSubmitted;
  String timeSubmitted;
  String grade;
  String status;
  String applicantRef;
  String applicationRef;
  String resumeRef;
  String applicantEmail;

  Applicant(
      this.name,
      this.dateSubmitted,
      this.timeSubmitted,
      this.grade,
      this.status,
      this.applicantRef,
      this.applicationRef,
      this.resumeRef,
      this.applicantEmail);
}
