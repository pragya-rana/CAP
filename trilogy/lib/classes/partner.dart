// This class represents a partner and their info.
class Partner {
  final String name;
  final String contactInfo;
  final String status;
  final String type;
  final String field;
  final String logo_url;
  final String cover_url;
  final String partnerId;
  final String location;
  final String description;

  Partner(
      this.name,
      this.contactInfo,
      this.status,
      this.type,
      this.field,
      this.logo_url,
      this.cover_url,
      this.partnerId,
      this.location,
      this.description);
}
